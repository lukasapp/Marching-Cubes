    public void Generate()
    {
        Debug.Log("RESTART: ");
        points = new int[size, size, size];
        vertices = new List<Vector3>();
        triangles = new List<int>();
        verticesArrayList = new List<Vector3[]>();
        trianglesArrayList = new List<int[]>();

        // Remove children.
        foreach (Transform child in transform)
        {
            Destroy(child.gameObject);
        }

        // DEBUG:
        x = 0;

        GenerateTerrainMap();

        // Only do this if the steps should not be render.
        if (debugMode == false)
        {
            CreateMeshTriangles();

            RenderMesh();
        }
    }

    // Start is called before the first frame update
    void Start()
    {
        Generate();
    }

    // Start is called before the first frame update
    void Update()
    {
        if (debugMode == true)
        {
            if(Input.GetKeyDown(KeyCode.Space))
            {
                if (x < size - 1)
                {
                    Debug.Log("X: " + x);

                    for (int y = 0; y < size - 1; y++)
                    {
                        for (int z = 0; z < size - 1; z++)
                        {
                            MarchCube(x, y, z);
                        }
                    }

                    RenderMesh();
                    x++;
                }
                else
                {
                    Debug.Log("DONE");
                }
            }
        }
    }

    private void OnDrawGizmos()
    {
        if (gizmos is null)
            return;

        for (int x = 0; x < size; x++)
            for (int y = 0; y < size; y++)
                for (int z = 0; z < size; z++)
                {
                    float gizmo = gizmos[x,y,z];
                    if(gizmo <= 0)
                        continue;
                    Gizmos.color = new Color(1f-gizmo, 1f-gizmo, 1f-gizmo, 0.4f);
                    Gizmos.DrawCube(new Vector3(x,y,z), Vector3.one);
                }
    }

    private void GenerateTerrainMap()
    {
        float max = 0.5f;
        float min = 0.5f;
        //gizmos = new float[size,size,size];

        for (int x = 0; x < size; x++)
            for (int y = 0; y < size; y++)
                for (int z = 0; z < size; z++)
                {
                    float point = Perlin3D(x, y, z, noiseOctaves-1, noisePersistence, noiseLacunarity);

                    //gizmos[x, y, z] = point >= surfaceLevel ? point : -1;

                    if (point > max)
                    {
                        max = point;
                    }

                    if (point < min)
                    {
                        min = point;
                    }

                    if (point >= surfaceLevel)
                    {
                        points[x, y, z] = 1; // Inside
                    }
                    else
                    {
                        points[x, y, z] = 0; // Outside
                    }
                }
    }

    private void CreateMeshTriangles()
    {
        for (int x = 0; x < size - 1; x++)
        { 
            for (int y = 0; y < size - 1; y++)
            { 
                for (int z = 0; z < size - 1; z++)
                {
                    MarchCube(x, y, z);

                    // Make sure the meshes size does not exceed the maximum size.
                    if (vertices.Count > 65_000)
                    {
                        verticesArrayList.Add(vertices.ToArray());
                        trianglesArrayList.Add(triangles.ToArray());

                        vertices = new List<Vector3>();
                        triangles = new List<int>();
                    }
                }
            }
        }

        if (vertices.Count > 0)
        {
            verticesArrayList.Add(vertices.ToArray());
            trianglesArrayList.Add(triangles.ToArray());
        }
    }

    private void MarchCube(int x, int y, int z)
    {
        // Get corners of cube.
        /*
                           5 ---- 6
                          /|     /|
        front top left-> 4-+----7 |
                         | 1----|-2
                         |/     |/
                         O ---- 3
         */
        int origin = points[x, y, z];
        int point1 = points[x, y, z + 1];
        int point2 = points[x + 1, y, z + 1];
        int point3 = points[x + 1, y, z];
        int point4 = points[x, y + 1, z];
        int point5 = points[x, y + 1, z + 1];
        int point6 = points[x + 1, y + 1, z + 1];
        int point7 = points[x + 1, y + 1, z];

        int[] corners = new int[8] { origin, point1, point2, point3, point4, point5, point6, point7 };

        // Debug.Log(origin+ " point1 " + point1 + " point2 " + point2 + " point3 " + point3 + " point4 " + point4 + " point5 " + point5 + " point6 " + point6 + " point7 " + point7);
        // Calculate the index.
        int cubeCfgIndex = 0;
        for (int i = 0; i < corners.Length; i++)
        {
            if (corners[i] == 1)
            {
                cubeCfgIndex |= 1 << i;
            }
        }

        // Now generate the triangles for this cube configuration
        Vector3 position = new Vector3(x, y, z);

        // process all triangle vertices in pairs of 3
        for (int triVertIdx = 0;;)
        {
            int edge1Index = TriangularTable[cubeCfgIndex, triVertIdx++];
            if (edge1Index == -1)
            {
                // -1 means this configuration is complete
                break;
            }

            int edge2Index = TriangularTable[cubeCfgIndex, triVertIdx++];
            int edge3Index = TriangularTable[cubeCfgIndex, triVertIdx++];

            Vector3 vertex1 = position + InterpolateEdge(edge1Index);
            Vector3 vertex2 = position + InterpolateEdge(edge2Index);
            Vector3 vertex3 = position + InterpolateEdge(edge3Index);

            // add the triangle vertices into the table in reverse order
            // because unity uses counter-clockwise vertex order, whereas
            // the vertex table uses clockwise vertex order.
            vertices.Add(vertex3);
            triangles.Add(vertices.Count - 1);

            vertices.Add(vertex2);
            triangles.Add(vertices.Count - 1);

            vertices.Add(vertex1);
            triangles.Add(vertices.Count - 1);
        }
    }

    private void RenderMesh()
    {
        for (int i = 0; i < verticesArrayList.Count; i++)
        {
            // create mesh
            Mesh mesh = new Mesh();
            mesh.vertices = verticesArrayList[i];
            mesh.triangles = trianglesArrayList[i];
            mesh.RecalculateNormals();

            // create container GameObject for the mesh
            GameObject g = new GameObject("MeshContainer" + i);
            g.transform.parent = transform;
            g.transform.position = transform.position;
            g.AddComponent<MeshFilter>().mesh = mesh;
            g.AddComponent<MeshRenderer>().material = terrainMaterial;

            vertices = new List<Vector3>();
            triangles = new List<int>();
        }
    }

    private float Perlin3D(
        float x, float y, float z,
        int subsamples = 0,
        float amplFalloff = 1f, float freqIncrease = 1f,
        float startAmplitude = 1f, float startFrequency = 1f
        )
    {
        Random.InitState(seed);
        var rngState = Random.state;

        float pointValue = 0f;
        float theoreticalMax = 0f;

        for (int i = 0; i <= subsamples; i++)
        {
            float rngOffset = Random.Range(-100_000f, 100_000f);

            pointValue += startAmplitude * Perlin3DNoise(
                ((x+offsetX) / noiseScale * startFrequency) + rngOffset,
                ((y+offsetY) / noiseScale * startFrequency) + rngOffset,
                ((z+offsetZ) / noiseScale * startFrequency) + rngOffset
                );
            theoreticalMax += startAmplitude;

            startAmplitude *= amplFalloff;
            startFrequency *= freqIncrease;
        }

        Random.state = rngState;
        return pointValue / theoreticalMax;
    }

    private static float Perlin3DNoise(float x, float y, float z)
    {
        float ab = Mathf.PerlinNoise(x, y);
        float bc = Mathf.PerlinNoise(y, z);
        float ac = Mathf.PerlinNoise(x, z);

        float ba = Mathf.PerlinNoise(y, x);
        float cb = Mathf.PerlinNoise(z, y);
        float ca = Mathf.PerlinNoise(z, x);

        float abc = ab + bc + ac + ba + cb + ca;
        return abc / 6f;
    }

