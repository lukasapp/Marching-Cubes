    private void GenerateTerrainMap()
    {
        float max = 0.5f;
        float min = 0.5f;
        //gizmos = new float[size,size,size];

        for (int x = 0; x < size; x++)
        {
            for (int y = 0; y < size; y++)
            {
                for (int z = 0; z < size; z++)
                {
                    float point = Perlin3D(x, y, z, noiseOctaves-1, noisePersistence, noiseLacunarity);

                    //gizmos[x, y, z] = point >= surfaceLevel ? point : -1;

                    if (point > max)
                    {
                        max = point;
                    }

                    if (point < min)
                    {
                        min = point;
                    }

                    if (point >= surfaceLevel)
                    {
                        points[x, y, z] = 1; // Inside
                    }
                    else
                    {
                        points[x, y, z] = 0; // Outside
                    }
                }
            }
        }
        Debug.Log("Min: " + min);
        Debug.Log("Max: " + max);
    }

    private void CreateMeshTriangles()
    {
        for (int x = 0; x < size - 1; x++)
        {
            for (int y = 0; y < size - 1; y++)
            {
                for (int z = 0; z < size - 1; z++)
                {
                    MarchCube(x, y, z);
                }
            }
        }
    }

    private void MarchCube(int x, int y, int z)
    {
        // Get corners of cube.
        int origin = points[x, y, z];
        int point1 = points[x, y, z + 1];
        int point2 = points[x + 1, y, z + 1];
        int point3 = points[x + 1, y, z];
        int point4 = points[x, y + 1, z];
        int point5 = points[x, y + 1, z + 1];
        int point6 = points[x + 1, y + 1, z + 1];
        int point7 = points[x + 1, y + 1, z];

        int[] corners = new int[8] { origin, point1, point2, point3, point4, point5, point6, point7 };

        // Debug.Log(origin+ " point1 " + point1 + " point2 " + point2 + " point3 " + point3 + " point4 " + point4 + " point5 " + point5 + " point6 " + point6 + " point7 " + point7);
        // Calculate the index.
        int triTableIndex = 0;
        for (int i = 0; i < corners.Length; i++)
        {
            if (corners[i] == 1)
            {
                triTableIndex |= 1 << i;
            }
        }

        // Now generate the triangles.
        Vector3 position = new Vector3(x, y, z);

        // Go through all 16 indicies.
        for (int i = 0; i < 16; i++)
        {
            int index = TriangularTable[triTableIndex, i];
            // -1 means we are done with it.
            if (index == -1)
            {
                break;
            }

            Vector3 vertexA = EdgeTable[index, 0];
            Vector3 vertexB = EdgeTable[index, 1];

            Vector3 vertexPosition = position + (vertexA + vertexB) / 2;

            vertices.Add(vertexPosition);
            triangles.Add(vertices.Count - 1);
        }
    }

    private void RenderMesh()
    {
        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
        mesh.RecalculateNormals();
        meshFilter.mesh = mesh;
    }

    private float Perlin3D(
        float x, float y, float z,
        int subsamples = 0,
        float amplFalloff = 1f, float freqIncrease = 1f,
        float startAmplitude = 1f, float startFrequency = 1f
        )
    {
        Random.InitState(seed);
        var rngState = Random.state;

        float pointValue = 0f;
        float theoreticalMax = 0f;

        for (int i = 0; i <= subsamples; i++)
        {
            float rngOffset = Random.Range(-100_000f, 100_000f);

            pointValue += startAmplitude * Perlin3DNoise(
                (x / noiseScale * startFrequency) + offsetX + rngOffset,
                (y / noiseScale * startFrequency) + offsetX + rngOffset,
                (z / noiseScale * startFrequency) + offsetX + rngOffset
                );
            theoreticalMax += startAmplitude;

            startAmplitude *= amplFalloff;
            startFrequency *= freqIncrease;
        }

        Random.state = rngState;
        return pointValue / theoreticalMax;
    }

    private static float Perlin3DNoise(float x, float y, float z)
    {
        float ab = Mathf.PerlinNoise(x, y);
        float bc = Mathf.PerlinNoise(y, z);
        float ac = Mathf.PerlinNoise(x, z);

        float ba = Mathf.PerlinNoise(y, x);
        float cb = Mathf.PerlinNoise(z, y);
        float ca = Mathf.PerlinNoise(z, x);

        float abc = ab + bc + ac + ba + cb + ca;
        return abc / 6f;
    }

